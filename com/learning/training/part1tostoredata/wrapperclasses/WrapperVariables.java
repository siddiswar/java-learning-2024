public class WrapperVariables {


    public static void main(String[] args) {

        int primitiveVar = 10; // primitive variable
        Integer wrapperVar = 12; //wrapper variable

        System.out.println("primitive variable value " + primitiveVar);
        System.out.println("wrapper variable value " + wrapperVar);


    }

}
