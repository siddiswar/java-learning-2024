package com.learning.training.part1tostoredata.strings;

public class SubStrings {
    public static void main(String[] args) {

        String fullName = "Sachin Ramesh Tendulkar";

        System.out.println(fullName.substring(5));      //n Ramesh Tendulkar
        System.out.println(fullName.substring(5,15));               //n Ramesh T

    }
}